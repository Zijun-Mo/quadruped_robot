def lin_speed_levels(
    env: ManagerBasedRLEnv,
    env_ids: Sequence[int],
    reward_term_name: str = "track_lin_vel_xy",
) -> torch.Tensor:
    """Curriculum for waypoint forward speed.

    Increments the upper bound of waypoint speed range while respecting the command's limit range.
    """

    command_term = env.command_manager.get_term("base_velocity")
    cfg = command_term.cfg

    current_range = getattr(cfg, "waypoint_speed_range", (0.5, 0.5))
    speed_step = getattr(cfg, "waypoint_speed_step", 0.1)
    speed_limit = getattr(cfg, "waypoint_speed_limit", None)
    if speed_limit is None:
        speed_limit = cfg.limit_ranges.lin_vel_x[1]

    reward_term = env.reward_manager.get_term_cfg(reward_term_name)
    reward = torch.mean(env.reward_manager._episode_sums[reward_term_name][env_ids]) / env.max_episode_length_s

    if env.common_step_counter % env.max_episode_length == 0:
        if reward > reward_term.weight * 0.8:
            new_max = torch.clamp(
                torch.tensor(current_range[1], device=env.device) + speed_step,
                max=speed_limit,
            ).item()
            cfg.waypoint_speed_range = (current_range[0], new_max)

    return torch.tensor(getattr(cfg, "waypoint_speed_range", current_range)[1], device=env.device)


